# Python uses lists to store multiple items into a single varibale
# The list is made up of single values

list1 = ['red', 'blue', 'green']
print(list1)

# Python will natively track the items in list with index identifiers
# These index identifiers are numeric starting from zero (0)
# You can reference an item in the list by index ID number

print(list1[2])
# This code should print 'green' from the list